void solve(int N, vector<int> &history, vector<int> &vst, int mx, int cnt, int &best) {
  if (cnt >= best) return;
  if (history.back() == N) { best = cnt; return; }

  // prune if upper bound is < N
  int upper = mx;
  for (int i = 0; i < best-cnt-1; i++) upper *= 2;
  if (upper < N) return;

  // get candidates
  vector<int> cand;
  for (auto x: history) {
    for (auto y: history) {
      if (!vst[x+y]) cand.push_back(x+y);
      if (x > y && !vst[x-y]) cand.push_back(x-y);
    }
  }
  sort(all(cand));
  cand.erase(unique(all(cand)), cand.end());

  // backtracking
  for (auto x: cand) {
    // multiply
    history.push_back(x);
    vst[x] = 1;
    solve(N, history, vst, max(mx, x), cnt+1, best);
    vst[x] = 0;
    history.pop_back();
  }
}

